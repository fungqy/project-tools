def blue_b(text):
    return f"<font color=\"blue\">** {text} **</font>"

def warn_b(text):
    return f"<font color=\"warning\">** {text} **</font>"

def warn(text):
    return f"<font color=\"warning\">{text}</font>"

def red(text):
    return f"<font color=\"red\">{text}</font>"

def grey(text):
    return f"<font color=\"#808080\">{text}</font>"